#include <stdio.h>
/**
 * calculator - includes five calculator functions
 * @a: first number
 * @b: second number
 * Return: the result of each equation
 */
int add(int a, int b)
{
	return (a + b);
}
int sub(int a, int b)
{
        return (a - b);
}
int mul(int a, int b)
{
        return (a * b);
}
int div(int a, int b)
{
        return (a / b);
}
int mod(int a, int b)
{
        return (a % b);
}
